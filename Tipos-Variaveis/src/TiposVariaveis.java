public class TiposVariaveis {
    public static void main(String[] args) {
        // Variável do tipo inteiro para armazenar a idade
        int idade = 30;

        // Variável do tipo double para armazenar o preço de um produto
        double preco = 99.99;

        // Variável do tipo float para armazenar a altura
        float altura = 1.75f;

        // Variável do tipo boolean para indicar se está ativo
        boolean ativo = true;

        // Variável do tipo char para armazenar a inicial do nome
        char inicial = 'P';

        // Variável do tipo String para armazenar o nome completo
        String nome = "Petrick Preite";

        // Constante para o número máximo de tentativas
        final int MAX_TENTATIVAS = 3;

        // Imprimindo os valores das variáveis
        System.out.println("Idade: " + idade);
        System.out.println("Preço: " + preco);
        System.out.println("Altura: " + altura);
        System.out.println("Ativo: " + ativo);
        System.out.println("Inicial: " + inicial);
        System.out.println("Nome: " + nome);
        System.out.println("Máximo de tentativas: " + MAX_TENTATIVAS);
    }
}
